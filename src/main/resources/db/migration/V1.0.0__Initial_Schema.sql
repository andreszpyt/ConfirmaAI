CREATE TABLE clinic (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255),
    whatsapp_phone VARCHAR(255),
    evolution_api_token VARCHAR(255),
    instance_name VARCHAR(255),
    webhook_token VARCHAR(255) NOT NULL,
    active BOOLEAN NOT NULL DEFAULT TRUE,
    confirmation_lead_time_hours INTEGER DEFAULT 24,
    msg_template_confirm VARCHAR(500),
    msg_template_cancel VARCHAR(500),
    msg_template_reschedule VARCHAR(500),
    CONSTRAINT uk_clinic_webhook_token UNIQUE (webhook_token)
);

CREATE TABLE patient (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255),
    whatsapp_phone VARCHAR(255)
);

CREATE TABLE appointment (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    clinic_id BIGINT NOT NULL,
    patient_id BIGINT NOT NULL,
    scheduled_at TIMESTAMP WITHOUT TIME ZONE,
    status VARCHAR(255) NOT NULL DEFAULT 'PENDING',
    quartz_job_id VARCHAR(255),
    communication_observation VARCHAR(512),
    CONSTRAINT fk_appointment_clinic FOREIGN KEY (clinic_id) REFERENCES clinic (id),
    CONSTRAINT fk_appointment_patient FOREIGN KEY (patient_id) REFERENCES patient (id)
);

